<br>
<br>
10. Bring these back to Al Shabim and he will ask you to make the weapon from them. Go north a bit and talk to the Bebadin Guard there. He will let you in the secret room to use the Experimental Anvil. Forge the weapon — it will be a dart tip. You may fail once or twice. Once the tip is made, use feathers with it to create a complete Experimental Dart. (Note: You may need to leave via the Shantay Pass to gather more supplies.)<br>
<br>
<br>
11. Take this to Al Shabim and he will give you the Pineapple and Six Darts. You now also have the ability to make DARTS! Go back to the Mining Camp.<br>
<br>
<br>
12. Go through the gates with NO Armor or Weapons equipped. Then down to the mines. Give the Pineapple to the Mercenary and go through the Mine Cave.<br>
<br>
<br>
13. Close to the mine cart is a bunch of Barrels. Search an empty one and take it. Now get in the mine cart. This may take a few tries depending on your agility level. (MAKE SURE you have food because you will get hurt.)<br>
<br>
<br>
