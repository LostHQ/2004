<br>
<br>
16. On the other side search the Barrels till you find Ana. Go to the Winch Bucket and use it. The Guard there will ask to help you. Agree. Ana will start complaining but you have to cover for her. Pick the statements that will compliment the Guard. (DO NOT attempt to walk through the Mine Cave — you will be caught and severely punished.)<br>
<br>
<br>
