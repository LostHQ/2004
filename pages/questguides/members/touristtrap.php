Levels Required: 10 Fletching, 20 Smithing. (High-level Woodcutting helps with the cactus.)<br>
<br>
Items Required: Desert gear (boots, robe, and shirt), Shantay Pass, knife, full or empty waterskin(s), hammer, 2-3 bronze bars, 10-30 feathers, at least 105gp, food, an amulet, a weapon. (Note: The only armor that won't burn you in the desert is a helmet, cape, and shield. However, you MUST unequip the helmet and shield around guards.)<br>
<br>
Rewards: Ana will give you a Wrought Iron Key useful for getting to a Mith & Addy mine in the Mining Camp. Irena will give you the choice of free exp in 2 skills - Agility, Thieving, Smithing, or Fletching. The higher your level, the better the experience. Also, you can choose the same skill twice and gain the ability to make darts.<br>
<br>
1. Start by talking to Irena. She is found through the Shantay Pass. You can pay Shantay 5gp to buy a pass first. Then go through and speak with Irena. (Note: Shantay also sells other items you will need for this quest.)<br>
<br>
<br>
2. After Irena tells you about her daughter Ana, she leaves you to find her on your own. Go south a little and examine the footsteps. They hint that you should head south (slightly west as well) to the Mining Camp. (MAKE SURE you find the Kharidian Cactus & cut them to keep water in your Waterskin[s].)<br>
<br>
<br>
3. Talk to the Mercenaries around there until you get them to admit that they don't like the Captain. This will cost you 5gp per bribe. Find out that the Captain doesn't like to fight his own battles. (If you get caught, they will take you to the desert and confiscate all but one waterskin.)<br>
<br>
<br>
4. Now talk to the Captain, get him roused until he tells you he will call his Guards. Pick the option about asking him to fight his own fights. Then he challenges you. Once he's dead a Metal Key will be added to your inventory. Use this to go inside with NO armor or weapons equipped! (ONCE you've killed the Captain you can come here to kill him again and get the key without all the dialogue.)<br>
<br>
<br>
5. Talk to the male slave who is trying to escape. After some conversation trade clothes with him. You will have to speak to him a second time to do so. (IF you get caught inside the camp the Guards & Mercenaries may throw you in jail.) The Cell Key can be found upstairs in Captain Siad’s desk.<br>
<br>
<br>
6. Now with your slave outfit on go downstairs through the huge door and follow the path to the TWO Mercenaries. Talk to one and ask him to let you trade spots. He will hint at you getting a Pineapple for him first. (MAKE SURE you don't have armor or weapons on around the Mercenaries.)<br>
<br>
7. Leave the camp completely. Go directly west until you find the Nomads' Camp. Talk to Al Shabim until you can ask him for a Pineapple. He will reveal the plan to get Blueprints from Captain Siad. Agree and he will give you a copy of the Chest Key. Now head back to the Mining Camp. (NOTE you can get more supplies from the nomads.)<br>
<br>
<br>
8. Inside the Mining Camp go upstairs in the building to Captain Siad's room. Search his desk to find the Cell Key. Then search the bookcases until you discover books about sailing. Now talk to him.<br>
<br>
<br>
