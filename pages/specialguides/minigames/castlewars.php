<h3>Strategies and Tactics</h3>
<p><b>Using the Catapult</b><br>
The catapult is an area weapon that hurls large rocks for very long distances. 
The term "area weapon" means that the target does not have to be 
struck by the rock to receive damage from it. Optimally the catapult should 
be manned by low level players. The catapult team's primary concern 
is to continuously fire rocks at the bridge and island area in order to inflict 
maximum damage to enemy soldiers crossing the bridge. Care should be taken, 
however, to ensure that as few team members are in the target area as possible.</p>
<img src="/img/special_guides/castlewars/catapult_controls.gif"> 
<p>The catapult is used by left clicking on while rocks are in the operator's 
inventory. Coordinates are set both visually and numerically by clicking on 
the left/right and the up/down arrows. Visual shots are good for adjusting 
fire on current real time events, while numeric coordinates should be taken 
for likely target area so that they can be quickly dialed in when needed.</p>
<p><b>Capturing Your Standard</b><br>
When a team captures their own standard, they can ensure that the opposing 
team does not score any points. This is done by killing the enemy that holds 
the standard outside of the castle. If the enemy holding the standard is killed 
inside the castle, it will be returned to its stand, but if they are killed 
outside the castle, the standard can be picked up and held for an indefinite 
amount of time. When this occurs, a team of high levels should be assigned 
to protecting the standard, so that the enemy does not recapture it. Medics 
and engineers will be needed on this team to re-supply bandages, setup barricades 
and extinguish fires.</p>
<p><b>Medics</b><br>
Medic should typically be low level players. Their sole responsibility is 
to re-supply warriors with bandages and to heal them while they are fighting. 
Any time there is a team of melee fighters, there should be a couple of medics 
assigned to the team.</p>
<p><b>Engineers</b><br>
Engineer is also a good low level position. The engineer's job is to 
set up barricades in strategic positions to assist fighters in detaining enemy 
intruders, removing barricades using explosive potions to assist team members 
in free passage, clear and collapse the tunnels at appropriate times to limit 
intruders and aid team members, and repair the main gate and the catapult. 
Engineers should carry a number of explosive potions and barricades, a tinderbox, 
some buckets of water, and a toolbox. Engineers should be assigned on all 
levels of the keep, in the dungeon at each tunnel entrance, and on the wall 
walk.</p>
<img src="/img/special_guides/castlewars/cw_explosions.png" width="362" height="148" alt="Explosions"> 
<p><b>Archers and Mages</b><br>
Archers and Mages should position themselves on the wall walk and on the four 
towers located on the island. Operating in teams is of vital importance to 
ensure effectiveness. Teams on the wall walk should watch both the interior 
of the castle and the exterior, and teams on the island towers should ambush 
and enemies that attempt to cross the island. Island teams should also utilize 
their long range abilities to assist melee teams.</p>
<p><b>Hunter/Killer Teams</b><br>
Hunter/Killer teams are the primary diversion for the enemy. These should 
be primarily mid level players. These teams will be primarily responsible 
for maintaining constant pressure on the enemy castle. While capturing the 
flag may be very tempting, these teams should only maintain control of the 
enemy's keep if it is taken and await the arrival of the capture team. 
This will ensure that only the strongest players are holding the enemy standard, 
so that having it recaptured by the enemy is as difficult as possible.</p>
<p><b>Protecting the Keep</b><br>
Protecting the Keep is one of the most vital positions available. These teams 
will consist of primarily middle and high level team members, with a few low 
level engineers and medics readily available. One of the most vital positions 
on these teams is the engineers. The engineers should be very watchful and 
place barricades to trap enemy in when they break through. Remember that only 
ten barricades can be placed at any one time, so effective use of as few barricades 
as possible is essential. Once trapped in, the melee teams should strike the 
intruders hard and constantly, wearing them down so they can be easily killed 
before they leave the castle.</p>
<p><b>Capturing the enemy Standard</b></p>
<p>There should be two teams of high level players that have the sole responsibility 
of capturing the enemy standard. These should be the strongest members of 
the team and will alternate movement between their castle and the enemy castle. 
Fast movement is essential to ensure that they are in place when the other 
team scores a point so they can capture the standard again and quickly move 
toward their castle. Fast re-supply of bandages and explosive potions is necessary, 
and little attention should be given to enemy attacks. These attacks should 
be handled by hunter/killer teams. Above all, the team member holding the 
standard should never stop to fight. Other members of the capture team will 
protect this team member as needed. Once the standard has been placed, the 
team should move directly to the enemy castle using the dungeon route, stopping 
in the armories to re-supply as needed.</p>
