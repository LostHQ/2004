<h3>Summary</h3>
<p>The primary objective of Castle Wars is to capture the enemy's flag 
as many times as possible in twenty minutes. While some team members may think 
that their job is not the best one and they want to score kills and capture 
the enemy flag, acting as an individual is the primary reason for losing the 
game. In the end the team that works together in the most organized and structured 
way will win and receive the reward. Needless fighting should be avoided at 
all costs and when fighting, only fight in teams. This ensures efficient and 
effective kills that waste as little resources as possible. In the end glory 
goes to the team that won and that is all anyone will remember, not the guy 
that got the most kills or captured the most standards. Those people cannot 
succeed without every team member performing their job to the highest standard.</p>
