<h3>Castle Wars Components</h3>
<p><b>The Castles</b></p>
<p><i>The Armoury</i><br>
<img src="/img/special_guides/castlewars/cw_store_room.png"><br>
The castles armoury is where all castle wars items are stored. This is an 
area that should be left open at all times so that team members can freely 
restock their supplies. This will also allow the enemy to access these supplies, 
so this is a good place for several mid level players and possibly a couple 
of high level players to position themselves in order to deter enemy re-supply. 
These people will also be able to guard the ladder leading to the dungeon 
and the ladder leading to the keep. Items available here are bandages, barricades, 
climbing ropes, explosive potions, pickaxes, rocks and toolkits. There is 
also a water source located on the wall next to the ladder leading to the 
dungeon.</p>
<p><i>The Keep</i><br>
The Keep is the main tower in the center of the castle that is the stronghold 
of the castle. The standard is kept on the top level of the keep. There should 
be numerous middle and high level team members positioned on all three levels 
of the keep, as well as a few low level members that will act as engineers 
and medics. The team spawn point is located on the first level of the keep. 
This will be a very handy tool for middle and low level players protecting 
this area.</p>
<p><i>The Curtain Wall</i><br>
The Curtain Wall is the large, outer wall of the castle. The catapult is located 
at the outer corner of the wall walk. The wall walk should be primarily manned 
by archers and mages, with a few middle level melee players on hand to deter 
intruders. There should also be a couple low level players available here 
for operating the catapult, including repair and re-supply. Archers and mages 
will use this vantage point for protecting the outer and inner areas of the 
castle. They should be set up in teams to minimize ammunition usage and maximize 
effectiveness with fast kills.</p>
<p>On the ground level, there should be a few teams of middle to high level 
players with the sole responsibility of protecting the entry to the castle. 
They should move quickly, killing one intruder at a time, maximizing effectiveness 
and efficiency.</p>
<p><b>The Moat</b><br>
The moat is the boundary between the land of Zamorak and the land of Saradomin. 
In the center of the moat is the island. There should be a couple of teams 
patrolling the moat to deter intruders from crossing using the jumping stones 
on either end of it.</p>
<p><b>The Island</b><br>
The Island has four towers on it that hold great tactical advantage. Optimally, 
a team of two or three archers or mages should be placed on each tower to 
battle the enemy crossing the island and to assist melee fighters in their 
battles. Archer/Mage teams should ensure that they have plenty of barricades 
in their possession to block the enemy from entering the towers.</p>
<p><b>The Dungeon</b><br>
The dungeon can be either an advantage or a disadvantage. At a minimum, there 
should be a couple teams of engineers, with pickaxes to clear and collapse 
the tunnels as needed. The dungeon can be a very good tool for ambushing the 
enemy castle and for returning the enemy's standard to score points.</p>

