<h3>Overview</h3>
<p><b>Introduction</b><br>
Castle Wars is a combat based team game lasting twenty minutes. It invites 
members to enter a battle between good and evil in a RuneScape style game 
of "Capture the Flag". Participants may choose to ally themselves 
with Saradomin or Zamorak, while followers of Guthix will be placed with both 
teams in order to ensure that each team is of equal size. During the course 
of this epic battle, participants may be required to scale tall curtain walls 
to surprise their enemy, use high explosives to demolish barricades and clear 
tunnels, use basic first aid to heal their fellow players, storm through heavily 
guarded areas to wear down the enemy's defences, or make the highest 
sacrifice of all by laying down their life to protect their standard. A select 
few will have the high honors of capturing the enemy's standard, scoring 
vital points for their team!</p>
<p>Getting there is very easy, and can be accomplished in two ways. The long 
route consists of traveling southwest from Ardougne, staying on the west side 
of the river, to the Castle Wars area. The faster and much more popular route 
